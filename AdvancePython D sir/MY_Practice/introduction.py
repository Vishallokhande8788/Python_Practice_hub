# This is a Python program that demonstrates modules, comments, escape sequences, and print statements.

# Importing a module (math module)
import math

# Using the math module to calculate the square root of 25
sqrt_value = math.sqrt(25)
print(f"The square root of 25 is {sqrt_value}.")

# Using escape sequences in strings
print("Hello, World!\nThis is a new line.")  # \n creates a new line
print("This is a tab:\tIndented text.")  # \t adds a tab space
print("This is a backslash: \\")  # \\ is used to display a backslash

# Variables and simple string
name = "Alice"
age = 25

# Comment: The following line prints a formatted string with variables
print(f"Hello, my name is {name} and I am {age} years old.")

# More examples of print formatting
print("First line.\nSecond line.")  # \n (new line)
print("This is a quote: \"Python is awesome!\"")  # Escape quote using \"